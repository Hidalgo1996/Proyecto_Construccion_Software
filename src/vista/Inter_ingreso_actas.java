    //Metodo para dar funcionalidad al boton ingreso de acta
    private void ingreso_acta(){
        
        Actas_partido acta_partido= new Actas_partido();//Instancia de tipo Actas_partido
        
        Controlador_actas_partido controlador_acta_partido= new Controlador_actas_partido();
        
        
        
        //Condicion para validar si los campos estan vacios
        if(id_text_box.getText().isEmpty() || hora_inicio_text_box.getText().isEmpty()
            || hora_fin_text_box.getText().isEmpty() || equipo_rival_text_box.getText().isEmpty()
            || equipo_local_text_box.getText().isEmpty() || duracion_partido_text_box.getText().isEmpty()
            || goles_rival_text_box.getText().isEmpty() || goles_local_text_box.getText().isEmpty()
            || equipo_ganador_text_box.getText().isEmpty() || id_arbitro_text_box.getText().isEmpty()){
            
            
            JOptionPane.showMessageDialog(null, "Primero llena todos los campos PAP√Å!!!");

            
        }else{
            
            //Esto me permite capturar lo que hay en las cajas de texto y guardar en los 
            //atributos de clase de actas_partido
            acta_partido.setId_acta_partido(Integer.parseInt(id_text_box.getText()));
            acta_partido.setHora_inicio_partido(hora_inicio_text_box.getText());
            acta_partido.setHora_fin_partido(hora_fin_text_box.getText());
            acta_partido.setNombre_equipo_rival(equipo_rival_text_box.getText().trim());
            acta_partido.setNombre_equipo_local(equipo_local_text_box.getText().trim());
            acta_partido.setDuracion_partido(Integer.parseInt(duracion_partido_text_box.getText()));
            acta_partido.setGoles_equipo_rival(Integer.parseInt(goles_rival_text_box.getText()));
            acta_partido.setGoles_equipo_local(Integer.parseInt(goles_local_text_box.getText()));
            acta_partido.setEquipo_ganador(equipo_ganador_text_box.getText().trim());
            acta_partido.setId_arbitro(Integer.parseInt(id_arbitro_text_box.getText()));
            
            
            //Condicion que me indica si guardaron los cambios
            if(controlador_acta_partido.guardar(acta_partido)){
                
                JOptionPane.showMessageDialog(null, "Registro exitoso");
            }else{
                
                JOptionPane.showMessageDialog(null, "Error de guardado, intentelo otra vez");
            }
        }
        
        //Limpieza de campos
            id_text_box.setText("");
            hora_inicio_text_box.setText("");
            hora_fin_text_box.setText("");
            equipo_rival_text_box.setText("");
            equipo_local_text_box.setText("");
            duracion_partido_text_box.setText("");
            goles_rival_text_box.setText("");
            goles_local_text_box.setText("");
            equipo_ganador_text_box.setText("");
            id_arbitro_text_box.setText("");

    }

