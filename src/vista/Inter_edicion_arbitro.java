    //Clase interna para poder agregar un fondo de imagen
    class FondoPanel extends JPanel{
        
        //Variable
        private Image imagen;
        
        
        //Sobreescritura del metodo Paint
        @Override
        public void paint(Graphics g){
            
            imagen = new ImageIcon(getClass().getResource("/imagenes/cesped_cancha_futbol.jpg")).getImage();
            
            //Dibujamos la imagen
            g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
            
            
            //Metodo set
            setOpaque(false);
            
            //Llamada al metodo de pintado
            super.paint(g);
        }
        
    }

